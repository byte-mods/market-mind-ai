class CausalDataCollector:
    """Assembles 10-year monthly panel from maintained tables.

    The panel is a pandas DataFrame with a DateTimeIndex and one column
    per node. All values are numeric floats. Missing months (e.g. GDP
    reported quarterly) are forward-filled.
    """

    def __init__(self) -> None:
        self._panel_cache: Optional[pd.DataFrame] = None
        self._cache_ts: float = 0.0
        self._cache_ttl: float = 3600  # 1 hour — tables change rarely

    def get_panel(self, force_refresh: bool = False) -> pd.DataFrame:
        now = time.time()
        if (not force_refresh and self._panel_cache is not None
                and (now - self._cache_ts) < self._cache_ttl):
            return self._panel_cache.copy()

        series: Dict[str, pd.Series] = {}
        for node_id, table in _TABLE_MAP.items():
            s = self._table_to_series(table, node_id)
            if not s.empty:
                series[node_id] = s

        panel = pd.concat(series, axis=1)
        panel.index = pd.to_datetime(panel.index)
        panel = panel.sort_index()
        panel = panel.ffill()  # forward-fill quarterly GDP to monthly
        panel = panel.dropna()  # drop leading months before all series start

        self._panel_cache = panel
        self._cache_ts = now
        return panel.copy()

    def get_current_values(self) -> Dict[str, float]:
        panel = self.get_panel()
        latest = panel.iloc[-1].to_dict()
        return {k: float(v) for k, v in latest.items()}

    def get_node_info(self) -> List[Dict[str, Any]]:
        current = self.get_current_values()
        nodes = []
        for node_id, meta in NODE_META.items():
            entry: Dict[str, Any] = {
                "id": node_id,
                "label": meta["label"],
                "unit": meta["unit"],
                "category": meta["category"],
                "current_value": current.get(node_id),
                "can_intervene": node_id in INTERVENTION_NODES,
                "can_target": node_id in TARGET_NODES,
            }
            nodes.append(entry)
        return nodes

    def get_returns_panel(self) -> pd.DataFrame:
        """Month-over-month log returns for structure learning."""
        panel = self.get_panel()
        return np.log(panel / panel.shift(1)).dropna()

    @staticmethod
    def _table_to_series(
        table: Tuple[Dict[str, Any], ...], name: str
    ) -> pd.Series:
        index = []
        values = []
        for row in table:
            v = row.get("value")
            if v is None:
                continue
            index.append(row["month"])
            values.append(float(v))
        return pd.Series(values, index=pd.DatetimeIndex(index), name=name)


_collector: Optional[CausalDataCollector] = None


def get_causal_data_collector() -> CausalDataCollector:
    global _collector
    if _collector is None:
        _collector = CausalDataCollector()
    return _collector
